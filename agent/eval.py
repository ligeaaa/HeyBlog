@dataclass(slots=True)
class _ConfusionPrediction:
    """Provide the attribute shape required by ``compute_confusion_counts``.

    Args:
        gold_label: Gold binary label for one covered row.
        pred_label: Final predicted binary label for the same row.
    """

    gold_label: str
    pred_label: str


@dataclass(slots=True)
class _TextDatasetRow:
    """Represent one preprocessed eval row with preloaded page text.

    Args:
        url: Original sample URL from the dataset.
        title: Sample title paired with the URL.
        label: Raw gold label from the dataset.
        text: Preloaded page text generated by the preprocessing step.
    """

    url: str
    title: str
    label: str
    text: str


def run_eval(
    *,
    csv_path: Path,
    settings: AgentSettings | None = None,
    provider: str | None = None,
    model: str | None = None,
    limit: int | None = None,
    max_text_chars: int = 12000,
    output_dir: Path | None = None,
    classifier: BlogClassifier | None = None,
) -> dict[str, Any]:
    """Run the eval for a preprocessed text-augmented CSV.

    Args:
        csv_path: Labeled CSV file with a required ``text`` column.
        settings: Optional runtime settings. Loaded from env when omitted.
        provider: Optional provider override.
        model: Optional model override.
        limit: Optional row limit for smoke tests.
        max_text_chars: Maximum number of page-text characters sent to the LLM
            per sample. Use ``0`` or a negative value to disable page text.
        output_dir: Optional explicit artifact directory.
        classifier: Optional injected classifier for tests.

    Returns:
        A dictionary containing the output directory, summary, and confusion
        matrix for the completed run.
    """
    started_at = perf_counter()
    resolved_settings = settings or AgentSettings.from_env()
    selected_provider = resolved_settings.resolve_provider(provider=provider, model=model)
    rows = _load_text_dataset_rows(csv_path)
    if limit is not None:
        rows = rows[:limit]

    inputs = [
        BlogJudgeInput(
            url=row.url,
            title=row.title,
            page_text=_truncate_text_for_llm(row.text, max_text_chars=max_text_chars),
        )
        for row in rows
    ]
    resolved_classifier = classifier or BlogClassifier(
        resolved_settings,
        selected_provider,
    )
    with tqdm(total=len(inputs), desc="Evaluating rows", unit="row") as progress:
        judge_results = asyncio.run(
            resolved_classifier.classify_many(
                inputs,
                progress_callback=progress.update,
            )
        )
    manifest_rows = _build_manifest(rows, judge_results=judge_results)
    summary = _build_summary(manifest_rows, eval_seconds=perf_counter() - started_at)
    confusion = build_confusion_matrix(
        compute_confusion_counts(
            [
                _ConfusionPrediction(gold_label=row.gold_label, pred_label=row.pred_label)
                for row in manifest_rows
                if row.pred_label is not None
            ]
        )
    )
    false_positive_rows = _build_error_rows(manifest_rows, error_type="fp")
    false_negative_rows = _build_error_rows(manifest_rows, error_type="fn")

    run_dir = _resolve_output_dir(resolved_settings, output_dir=output_dir)
    ensure_dir(run_dir)
    write_json(run_dir / "summary.json", summary.to_dict())
    write_json(run_dir / "confusion_matrix.json", confusion)
    write_jsonl(run_dir / "manifest.jsonl", [row.to_dict() for row in manifest_rows])
    write_jsonl(run_dir / "false_positives.jsonl", false_positive_rows)
    write_jsonl(run_dir / "false_negatives.jsonl", false_negative_rows)
    write_csv(
        run_dir / "false_positives.csv",
        fieldnames=["url", "title", "gold_label", "pred_label"],
        rows=false_positive_rows,
    )
    write_csv(
        run_dir / "false_negatives.csv",
        fieldnames=["url", "title", "gold_label", "pred_label"],
        rows=false_negative_rows,
    )
    return {
        "output_dir": str(run_dir),
        "summary": summary.to_dict(),
        "confusion_matrix": confusion,
        "false_positives": false_positive_rows,
        "false_negatives": false_negative_rows,
    }


def _build_manifest(
    rows: list[_TextDatasetRow],
    *,
    judge_results: list[Any],
) -> list[EvalManifestRow]:
    """Combine preprocessed dataset rows and LLM results into one manifest.

    Args:
        rows: Preprocessed dataset rows loaded from the CSV.
        judge_results: Ordered classifier results aligned to ``rows``.

    Returns:
        Ordered per-row manifest records for the whole run.
    """
    mapping = default_mapping()
    manifest: list[EvalManifestRow] = []
    for row, judge_result in zip(rows, judge_results, strict=True):
        gold_label = _map_raw_label(row.label, mapping)
        used_page_content = bool(row.text.strip())
        manifest.append(
            EvalManifestRow(
                url=row.url,
                title=row.title,
                gold_label=gold_label,
                pred_label=judge_result.pred_label,
                fetch_status="preloaded",
                error_kind=None,
                used_page_content=used_page_content,
                reason=judge_result.reason,
                final_url=None,
                provider=judge_result.provider,
                model=judge_result.model,
                llm_status=judge_result.llm_status,
            )
        )
    return manifest


def _map_raw_label(raw_label: str, mapping: Any) -> str:
    """Map one raw CSV label into the binary eval label space.

    Args:
        raw_label: Raw label string from the dataset CSV.
        mapping: Existing trainer label mapping object.

    Returns:
        ``blog`` or ``non_blog`` for the current dataset semantics.
    """
    bucket = mapping.classify(raw_label)
    if bucket == "positive":
        return "blog"
    if bucket == "negative":
        return "non_blog"
    raise ValueError(f"raw label {raw_label!r} does not map into the current binary eval space")


def _build_summary(rows: list[EvalManifestRow], *, eval_seconds: float) -> EvalSummary:
    """Compute summary metrics and coverage from the per-row manifest.

    Args:
        rows: Per-row manifest records for one eval run.
        eval_seconds: Total wall-clock seconds spent in the eval run.

    Returns:
        A summary dataclass with counts, metrics, and coverage values.
    """
    counted_rows = [
        _ConfusionPrediction(gold_label=row.gold_label, pred_label=row.pred_label)
        for row in rows
        if row.pred_label is not None
    ]
    counts = compute_confusion_counts(counted_rows)
    rows_with_page_content = sum(1 for row in rows if row.used_page_content)
    rows_with_pred_label = len(counted_rows)
    precision = _safe_divide(counts["tp"], counts["tp"] + counts["fp"])
    recall = _safe_divide(counts["tp"], counts["tp"] + counts["fn"])
    f1 = _safe_divide(2 * precision * recall, precision + recall)
    accuracy = _safe_divide(counts["tp"] + counts["tn"], rows_with_pred_label)
    total_rows = len(rows)
    return EvalSummary(
        total_rows=total_rows,
        rows_with_pred_label=rows_with_pred_label,
        rows_with_page_content=rows_with_page_content,
        tp=counts["tp"],
        tn=counts["tn"],
        fp=counts["fp"],
        fn=counts["fn"],
        precision=round(precision, 6),
        recall=round(recall, 6),
        f1=round(f1, 6),
        accuracy=round(accuracy, 6),
        page_fetch_coverage=round(_safe_divide(rows_with_page_content, total_rows), 6),
        classification_coverage=round(_safe_divide(rows_with_pred_label, total_rows), 6),
        eval_seconds=round(eval_seconds, 6),
    )


def _build_error_rows(rows: list[EvalManifestRow], *, error_type: str) -> list[dict[str, str]]:
    """Extract false-positive or false-negative samples from the manifest.

    Args:
        rows: Per-row manifest records for one eval run.
        error_type: Either ``fp`` or ``fn``.

    Returns:
        A list of compact rows containing the sample URL, title, gold label,
        and predicted label for the requested error type.
    """
    if error_type not in {"fp", "fn"}:
        raise ValueError(f"unsupported error_type: {error_type!r}")
    result: list[dict[str, str]] = []
    for row in rows:
        if row.pred_label is None:
            continue
        is_false_positive = row.gold_label != "blog" and row.pred_label == "blog"
        is_false_negative = row.gold_label == "blog" and row.pred_label != "blog"
        if (error_type == "fp" and is_false_positive) or (error_type == "fn" and is_false_negative):
            result.append(
                {
                    "url": row.url,
                    "title": row.title,
                    "gold_label": row.gold_label,
                    "pred_label": row.pred_label,
                }
            )
    return result


def _safe_divide(numerator: float, denominator: float) -> float:
    """Safely divide two numbers for metric calculations."""
    if denominator == 0:
        return 0.0
    return numerator / denominator


def _resolve_output_dir(settings: AgentSettings, *, output_dir: Path | None) -> Path:
    """Choose the artifact directory for the eval run.

    Args:
        settings: Agent settings that provide the default output root.
        output_dir: Optional explicit artifact directory.

    Returns:
        One directory path where summary and manifest artifacts should be
        written.
    """
    if output_dir is not None:
        return output_dir
    stamp = datetime.now(UTC).strftime("%Y%m%dT%H%M%SZ")
    return settings.output_root / stamp


def _load_text_dataset_rows(csv_path: Path) -> list[_TextDatasetRow]:
    """Load a preprocessed dataset and require the presence of the ``text`` column.

    Args:
        csv_path: Path to the preprocessed CSV dataset.

    Returns:
        Parsed rows containing ``url``, ``title``, ``label``, and ``text``.

    Raises:
        ValueError: If the dataset does not contain the required ``text`` column.
    """
    _set_csv_field_size_limit()
    with csv_path.open("r", encoding="utf-8", newline="") as handle:
        reader = csv.DictReader(handle)
        fieldnames = reader.fieldnames or []
        if "text" not in fieldnames:
            raise ValueError(
                "dataset is missing required 'text' column; run "
                "`python -m data_preprocessing.build_agent_text_dataset` first"
            )
        rows: list[_TextDatasetRow] = []
        for raw in reader:
            rows.append(
                _TextDatasetRow(
                    url=str(raw.get("url", "")).strip(),
                    title=str(raw.get("title", "")).strip(),
                    label=str(raw.get("label", "")).strip(),
                    text=_deserialize_text_field(str(raw.get("text", ""))),
                )
            )
    return rows


def _set_csv_field_size_limit() -> None:
    """Raise Python's CSV field limit so large text payloads can be loaded.

    Returns:
        None. The function updates Python's process-global CSV parser limit.
    """
    limit = sys.maxsize
    while limit > 0:
        try:
            csv.field_size_limit(limit)
            return
        except OverflowError:
            limit //= 10


def _truncate_text_for_llm(text: str, *, max_text_chars: int) -> str | None:
    """Trim one page-text field before passing it to the classifier.

    Args:
        text: Preloaded page text from the dataset.
        max_text_chars: Maximum number of characters to keep. Non-positive
            values disable page-text forwarding.

    Returns:
        The trimmed text, or ``None`` when page text should be omitted.
    """
    normalized = text.strip()
    if not normalized or max_text_chars <= 0:
        return None
    return normalized[:max_text_chars]


def _deserialize_text_field(text: str) -> str:
    """Restore serialized single-line text back into model-readable content.

    Args:
        text: Serialized dataset field where newlines may be stored as literal
            escape sequences such as ``\\n``.

    Returns:
        The decoded text content for prompt construction. If the field is not a
        valid escaped payload, the original string is returned unchanged.
    """
    if "\\" not in text:
        return text
    try:
        return json.loads(f'"{text}"')
    except json.JSONDecodeError:
        return text


def _build_arg_parser() -> argparse.ArgumentParser:
    """Build the CLI parser for running the eval module directly."""
    default_csv = DEFAULT_DATA_ROOT / "blog-label-training-2026-04-11-with-text.csv"
    parser = argparse.ArgumentParser(description="Run the lightweight agent blog eval.")
    parser.add_argument(
        "--csv",
        type=Path,
        default=default_csv,
        help=f"Path to the labeled CSV to evaluate. Defaults to {default_csv}.",
    )
    parser.add_argument(
        "--provider",
        type=str,
        default="deepseek",
        help="Optional provider override. Defaults to AGENT_DEFAULT_PROVIDER from env.",
    )
    parser.add_argument(
        "--model",
        type=str,
        default="deepseek-v4-flash",
        help="Optional model override. Defaults to AGENT_DEFAULT_MODEL or the provider default from env.",
    )
    parser.add_argument("--limit", type=int, default=None, help="Optional row limit for smoke tests.")
    parser.add_argument(
        "--max-text-chars",
        type=int,
        default=12000,
        help="Maximum number of page-text characters sent to the LLM per row. Defaults to 12000.",
    )
    parser.add_argument("--output-dir", type=Path, default=None, help="Optional explicit artifact directory.")
    return parser


def main() -> None:
    """Run the CLI entrypoint for the lightweight agent eval."""
    args = _build_arg_parser().parse_args()
    result = run_eval(
        csv_path=args.csv,
        provider=args.provider,
        model=args.model,
        limit=args.limit,
        max_text_chars=args.max_text_chars,
        output_dir=args.output_dir,
    )
    print(result["output_dir"])


if __name__ == "__main__":
    main()
